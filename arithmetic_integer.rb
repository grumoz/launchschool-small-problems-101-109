puts "#{first} * #{sec} = #{prod}"
div = first / sec
puts "#{first} / #{sec} = #{div}"
mod = first % sec
puts "#{first} % #{sec} = #{mod}"
deg = first ** sec
puts "#{first} **  #{sec} = #{deg}"


